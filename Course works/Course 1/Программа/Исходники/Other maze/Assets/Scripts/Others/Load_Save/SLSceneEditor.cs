[CustomEditor(typeof(SLScene))]
public class SLSceneEditor : Editor
{
    SLScene CurrentSL;
    private void OnEnable()
    {
         CurrentSL = (SLScene)target; 
    }

    public override void OnInspectorGUI()
    {
        CurrentSL.SceneDefault = EditorGUILayout.TextField("Имя начальной сцены", CurrentSL.SceneDefault);

        CurrentSL.SaveType = (SLScene.SavingType)EditorGUILayout.EnumPopup("Способ сохранения",CurrentSL.SaveType);

        if (CurrentSL.SaveType == SLScene.SavingType.Time||CurrentSL.SaveType==SLScene.SavingType.TimeAndTrigger)
            CurrentSL.TimeToSave = EditorGUILayout.FloatField("Интервал между сохранениями", CurrentSL.TimeToSave);
    }
}
