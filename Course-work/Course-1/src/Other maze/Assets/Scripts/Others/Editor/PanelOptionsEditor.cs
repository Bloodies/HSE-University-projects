[CustomEditor(typeof(PanelOptions))]
public class PanelOptionsEditor : Editor
{

	private PanelOptions panelOptions;
	private SerializedObject script;

	public SerializedProperty rows;
	ReorderableList rows2;

	public SerializedProperty spaceDistance;
	public SerializedProperty xAxis;
	public SerializedProperty yAdjust;

	public void OnEnable()
    {
		panelOptions = (PanelOptions)target;
		script = new SerializedObject (target);

		// Objects Settings
		rows = script.FindProperty ("rows");
		this.rows2 = new ReorderableList (script, rows);

		spaceDistance = script.FindProperty ("spaceDistance");
		xAxis = script.FindProperty ("xAxis");
		yAdjust = script.FindProperty ("yAdjust");

		this.rows2.drawElementCallback = RectNewRows;
		this.rows2.drawHeaderCallback = (Rect rect) => {
			EditorGUI.LabelField (rect, "Rows");
		};
	}

	public override void OnInspectorGUI()
    {
		script.Update ();
		EditorGUI.BeginChangeCheck ();
		//EditorGUILayout.PropertyField (rows);
		rows2.DoLayoutList ();
		EditorGUILayout.PropertyField (spaceDistance);
		EditorGUILayout.PropertyField (xAxis);
		EditorGUILayout.PropertyField (yAdjust);

		if (GUILayout.Button ("Organize Rows"))
        {
			GUI.FocusControl (null);
			panelOptions.OrganizeRows ();
		}

		if (EditorGUI.EndChangeCheck ())
        {
			script.ApplyModifiedProperties ();
			//GUI.FocusControl (null);
		}
	}

	private void RectNewRows(Rect rect, int index, bool active, bool focus)
    {
		EditorGUI.PropertyField (new Rect(rect.x, rect.y, rect.width, 16), rows.GetArrayElementAtIndex (index));
	}
}
