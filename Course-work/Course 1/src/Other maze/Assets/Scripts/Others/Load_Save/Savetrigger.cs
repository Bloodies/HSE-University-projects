public class Savetrigger : MonoBehaviour
{
    private void OnTriggerEnter2D(Collider2D collision)
    {
        if (SLScene.SavingSystem.SaveType != SLScene.SavingType.Time)
        {
            print("Сохранение тригером");
            SLScene.SavingSystem.SaveScene();
        }
    }
}
